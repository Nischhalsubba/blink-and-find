function setText(element, text) {
  if (!element || !text) return;
  element.textContent = text;
}

function createEl(tag, className, text) {
  const el = document.createElement(tag);
  if (className) el.className = className;
  if (text) el.textContent = text;
  return el;
}

function buildSnapshotGrid(project) {
  const grid = createEl('div', 'snapshot-grid nrs-case-study-snapshot');
  Object.entries(project.snapshot).forEach(([label, value]) => {
    const item = createEl('div');
    const heading = createEl('h5', null, label);
    heading.style.color = 'var(--text-tertiary)';
    heading.style.marginBottom = '8px';
    heading.style.textTransform = 'uppercase';
    const body = createEl('p', null, value);
    item.append(heading, body);
    grid.appendChild(item);
  });
  return grid;
}

function buildDecisionCards(project) {
  const grid = createEl('div', 'journey-grid nrs-case-study-decisions');
  project.decisions.forEach(([decision, why, tradeoff, result]) => {
    const card = createEl('article', 'journey-card');
    card.append(
      createEl('span', 'eyebrow', 'Decision'),
      createEl('h3', null, decision),
      createEl('p', null, `Why: ${why}`),
      createEl('p', null, `Tradeoff: ${tradeoff}`),
      createEl('p', null, `Result: ${result}`),
    );
    grid.appendChild(card);
  });
  return grid;
}

function buildList(items) {
  const list = createEl('ul', 'case-list');
  items.forEach((item) => list.appendChild(createEl('li', null, item)));
  return list;
}

function renderCaseStudy(project) {
  const main = document.querySelector('main.container');
  if (!main) return;

  const hero = main.querySelector('.hero-section');
  if (hero) {
    setText(hero.querySelector('.hero-title'), project.title);
    setText(hero.querySelector('.body-large, .section-lead'), project.summary);
    const chipWrap = hero.querySelector('.case-meta-chips');
    if (chipWrap) {
      chipWrap.replaceChildren(...project.chips.map((chip) => createEl('span', 'badge-pill', chip)));
    }
  }

  const image = main.querySelector('.case-hero-img-container');
  const proof = main.querySelector('#proof');
  const closing = Array.from(main.querySelectorAll('.section-container')).slice(-1)[0];

  const content = document.createDocumentFragment();

  const snapshotSection = createEl('section', 'section-container reveal-on-scroll');
  snapshotSection.appendChild(buildSnapshotGrid(project));
  content.appendChild(snapshotSection);

  const contextSection = createEl('section', 'section-container reveal-on-scroll');
  contextSection.append(
    createEl('div', 'case-label', 'Context'),
    createEl('h2', 'section-title', 'Project context'),
    createEl('p', 'body-large', project.context),
  );
  content.appendChild(contextSection);

  const roleSection = createEl('section', 'section-container reveal-on-scroll');
  roleSection.append(
    createEl('div', 'case-label', 'Role'),
    createEl('h2', 'section-title', 'My role and scope'),
    createEl('p', 'body-large', project.role),
  );
  content.appendChild(roleSection);

  const problemSection = createEl('section', 'section-container reveal-on-scroll');
  problemSection.append(
    createEl('div', 'case-label', 'Problem'),
    createEl('h2', 'section-title', 'What needed to be made clearer'),
    createEl('p', 'body-large', project.problem),
  );
  content.appendChild(problemSection);

  const decisionSection = createEl('section', 'section-container reveal-on-scroll');
  decisionSection.append(
    createEl('div', 'case-label', 'Design decisions'),
    createEl('h2', 'section-title', 'Key decisions'),
    buildDecisionCards(project),
  );
  content.appendChild(decisionSection);

  const solutionSection = createEl('section', 'section-container reveal-on-scroll');
  solutionSection.append(
    createEl('div', 'case-label', 'Final solution'),
    createEl('h2', 'section-title', 'What the final design covered'),
    createEl('p', 'body-large', project.finalSolution),
  );
  content.appendChild(solutionSection);

  const impactSection = createEl('section', 'section-container reveal-on-scroll');
  impactSection.append(
    createEl('div', 'case-label', 'Impact'),
    createEl('h2', 'section-title', 'Honest impact'),
    createEl('p', 'body-large', project.impact),
  );
  content.appendChild(impactSection);

  const visualSection = createEl('section', 'section-container reveal-on-scroll');
  visualSection.append(
    createEl('div', 'case-label', 'Visual story plan'),
    createEl('h2', 'section-title', 'What to show in the walkthrough'),
    buildList(project.visualPlan),
  );
  content.appendChild(visualSection);

  const reflectionSection = createEl('section', 'section-container reveal-on-scroll');
  reflectionSection.append(
    createEl('div', 'case-label', 'Reflection'),
    createEl('h2', 'section-title', 'What this project shows'),
    createEl('p', 'body-large', project.reflection),
  );
  content.appendChild(reflectionSection);

  Array.from(main.children).forEach((child) => {
    if (child === hero || child === image || child === proof || child === closing) return;
    child.remove();
  });

  if (image) image.after(content);
  else if (hero) hero.after(content);
}

function polishProjectCards() {
  document.querySelectorAll('.project-card').forEach((card) => {
    const title = card.querySelector('h3')?.textContent?.trim();
    if (!title || !CARD_COPY[title]) return;
    const [eyebrow, summary] = CARD_COPY[title];
    setText(card.querySelector('.eyebrow'), eyebrow);
    setText(card.querySelector('.card-summary'), summary);
  });
}

function addAllWorkNote() {
  const projectsPage = window.location.pathname === '/projects.html';
  if (!projectsPage) return;
  const grid = document.querySelector('.project-grid');
  if (!grid || document.querySelector('.nrs-all-work-note')) return;

  const note = createEl('section', 'section-container nrs-all-work-note');
  note.innerHTML = `
    <div class="case-label">Additional work and experience</div>
    <h2 class="section-title">Other confirmed work</h2>
    <p class="body-large">This portfolio also reflects experience across MAS DataHub, Idealaya, Tegzy, ESR Tech, ThemeGrill, Gurzu, and Diagonal Softwares where relevant. Those entries should be expanded into full case studies only when project-specific scope, assets, and constraints are available.</p>
  `;
  grid.closest('section')?.after(note);
}

export function polishPortfolioCaseStudies() {
  const project = PROJECTS[window.location.pathname];
  polishProjectCards();
  addAllWorkNote();
  if (project) renderCaseStudy(project);
}
